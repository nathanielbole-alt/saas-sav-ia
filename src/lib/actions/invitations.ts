// ---------------------------------------------------------------------------

export async function revokeInvitation(
  invitationId: string
): Promise<{ success: boolean; error?: string }> {
  const profile = await getAuthProfile()
  if (!profile) return { success: false, error: 'Non authentifie' }

  if (profile.role !== 'owner' && profile.role !== 'admin') {
    return { success: false, error: 'Seuls les proprietaires et administrateurs peuvent revoquer' }
  }

  const { error } = await supabaseAdmin
    .from('invitations')
    .update({ status: 'revoked', updated_at: new Date().toISOString() })
    .eq('id', invitationId)
    .eq('organization_id', profile.organization_id)
    .eq('status', 'pending')

  if (error) {
    return { success: false, error: error.message }
  }

  revalidatePath('/dashboard/settings')
  return { success: true }
}

// ---------------------------------------------------------------------------
// acceptInvitation
// ---------------------------------------------------------------------------

export async function acceptInvitation(
  token: string
): Promise<{ success: boolean; error?: string }> {
  const supabase = await createClient()
  const {
    data: { user },
  } = await supabase.auth.getUser()

  if (!user) return { success: false, error: 'Non authentifie' }

  // Fetch invitation by token using admin (bypasses RLS)
  const { data: invitation } = await supabaseAdmin
    .from('invitations')
    .select('id, organization_id, email, role, status, expires_at')
    .eq('token', token)
    .single()

  const inv = invitation as {
    id: string
    organization_id: string
    email: string
    role: 'owner' | 'admin' | 'agent'
    status: string
    expires_at: string
  } | null

  if (!inv) {
    return { success: false, error: 'Invitation introuvable' }
  }

  if (inv.status !== 'pending') {
    return { success: false, error: 'Cette invitation n\'est plus valide' }
  }

  if (new Date(inv.expires_at) < new Date()) {
    // Auto-expire
    await supabaseAdmin
      .from('invitations')
      .update({ status: 'expired', updated_at: new Date().toISOString() })
      .eq('id', inv.id)
    return { success: false, error: 'Cette invitation a expire' }
  }

  // Verify email matches
  if (user.email?.toLowerCase() !== inv.email.toLowerCase()) {
    return {
      success: false,
      error: `Cette invitation est destinee a ${inv.email}. Connectez-vous avec ce compte.`,
    }
  }

  // Update profile: assign to org + role
  const { error: profileError } = await supabaseAdmin
    .from('profiles')
    .update({
      organization_id: inv.organization_id,
      role: inv.role,
      is_onboarded: true,
    })
    .eq('id', user.id)

  if (profileError) {
    return { success: false, error: profileError.message }
  }

  // Mark invitation as accepted
  await supabaseAdmin
    .from('invitations')
    .update({ status: 'accepted', updated_at: new Date().toISOString() })
    .eq('id', inv.id)

  revalidatePath('/dashboard')
  revalidatePath('/dashboard/settings')
  return { success: true }
}

